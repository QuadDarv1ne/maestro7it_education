def main():
    """
    Основная функция программы для вычисления площади круга по заданному радиусу.
    
    Функция выполняет следующие действия:
    1. Запрашивает у пользователя радиус окружности в сантиметрах.
    2. Проверяет корректность введённого значения (неотрицательное число).
    3. Вычисляет площадь круга по формуле: S = π × r², где π = 3.141.
    4. Округляет результат до двух знаков после запятой.
    5. Выводит результат в формате:
       "Если радиус окружности равен {r} сантиметров, то площадь круга равна {area}".
    
    Если введён отрицательный радиус, функция выводит сообщение об ошибке.
    
    Пример использования:
    >>> main()
    Введите радиус окружности в сантиметрах: 5
    Если радиус окружности равен 5.0 сантиметров, то площадь круга равна 78.52
    
    Возвращает:
        None
    """
    pi = 3.141
    
    r = float(input("Введите радиус окружности в сантиметрах: "))
    
    if r >= 0:
        area = pi * r ** 2
        area = round(area, 2)
        print(f"Если радиус окружности равен {r} сантиметров, то площадь круга равна {area}")
    else:
        print("Радиус не может быть отрицательным")

if __name__ == "__main__":
    main()