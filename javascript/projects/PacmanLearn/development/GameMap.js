    countFood(map) {
        let count = 0;
        for (let y = 0; y < map.length; y++) {
            for (let x = 0; x < map[y].length; x++) {
                if (map[y][x] === 2 || map[y][x] === 3) {
                    count++;
                }
            }
        }
        return count;
    }
