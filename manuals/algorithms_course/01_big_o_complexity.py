"""
1. СЛОЖНОСТЬ АЛГОРИТМОВ (BIG O NOTATION)

Это способ измерения того, как быстро работает алгоритм (время выполнения)
или сколько памяти он потребляет (memory complexity) при увеличении входных данных.

Зачем это нужно?
Чтобы понимать, "упадёт" ли твой код, если на вход вместо 10 элементов придёт 10 миллионов.

Основные виды сложности (от лучшей к худшей):
O(1) — Константная: время не зависит от размера данных.
        Пример: получение элемента по индексу arr[5], вычисление len(list).
O(log n) — Логарифмическая: алгоритм каждый раз отсекает половину данных.
           Пример: бинарный поиск.
O(n) — Линейная: время пропорционально количеству данных.
       Пример: линейный поиск, один проход по массиву.
O(n log n) — Линейно-логарифмическая: стандарт для хороших сортировок.
             Пример: list.sort() в Python (Timsort), Merge Sort, Quick Sort.
O(n²) — Квадратичная: часто встречается при вложенных циклах.
        Пример: пузырьковая сортировка, поиск всех пар.
O(2ⁿ) — Экспоненциальная: время удваивается с каждым элементом.
        Пример: рекурсивное вычисление Фибоначчи без мемоизации.

Правила вычисления Big O:
1. Отбрасываем константы: O(2n) = O(n), O(500) = O(1)
2. Отбрасываем младшие члены: O(n² + n) = O(n²)
3. Последовательные операции складываются: O(n) + O(n log n) = O(n log n)
4. Вложенные операции перемножаются: O(n) × O(n) = O(n²)

Оценка допустимой сложности по размеру данных:
n ≤ 10      → O(n!)     полный перебор
n ≤ 20      → O(2ⁿ)     перебор подмножеств
n ≤ 100     → O(n³)     три вложенных цикла
n ≤ 1000    → O(n²)     два вложенных цикла
n ≤ 10⁵     → O(n log n) или O(n)
n ≤ 10⁶     → O(n)      линейный проход
n > 10⁶     → O(log n)  или O(1)
"""


def analyze_complexity():
    """
    Демонстрация различных классов сложности на примерах кода.
    """
    
    # Пример O(1) — константная сложность
    def get_first_element(arr):
        """Доступ по индексу — O(1)"""
        return arr[0]
    
    # Пример O(log n) — логарифмическая сложность
    def count_bits(n):
        """Подсчёт бит в числе — O(log n)"""
        count = 0
        while n > 0:
            count += n & 1
            n >>= 1
        return count
    
    # Пример O(n) — линейная сложность
    def find_max(arr):
        """Поиск максимума — O(n)"""
        max_val = arr[0]
        for val in arr:
            if val > max_val:
                max_val = val
        return max_val
    
    # Пример O(n log n) — линейно-логарифмическая сложность
    def merge_sort_example(arr):
        """Сортировка слиянием — O(n log n)"""
        if len(arr) <= 1:
            return arr
        mid = len(arr) // 2
        left = merge_sort_example(arr[:mid])
        right = merge_sort_example(arr[mid:])
        return merge(left, right)
    
    def merge(left, right):
        """Слияние двух отсортированных массивов"""
        result = []
        i = j = 0
        while i < len(left) and j < len(right):
            if left[i] <= right[j]:
                result.append(left[i])
                i += 1
            else:
                result.append(right[j])
                j += 1
        result.extend(left[i:])
        result.extend(right[j:])
        return result
    
    # Пример O(n²) — квадратичная сложность
    def find_all_pairs(arr):
        """Поиск всех пар — O(n²)"""
        pairs = []
        for i in range(len(arr)):
            for j in range(i + 1, len(arr)):
                pairs.append((arr[i], arr[j]))
        return pairs
    
    # Пример O(2ⁿ) — экспоненциальная сложность
    def fibonacci(n):
        """Числа Фибоначчи без мемоизации — O(2ⁿ)"""
        if n <= 1:
            return n
        return fibonacci(n - 1) + fibonacci(n - 2)


