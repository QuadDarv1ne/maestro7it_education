"""
БИТОВЫЕ ОПЕРАЦИИ (BIT MANIPULATION)

Битовые операции работают напрямую с битами чисел.
Это один из самых быстрых способов решения многих задач.

Основные операции:
&   — побитовое И
|   — побитовое ИЛИ
^   — побитовое XOR (исключающее ИЛИ)
~   — побитовое НЕ
<<  — сдвиг влево
>>  — сдвиг вправо

Практические приёмы:
n & 1         — проверка нечётности
n & (n-1)     — сброс младшего бита
n & -n        — выделение младшего бита
n | (1 << k)  — установка k-го бита
n & ~(1 << k) — сброс k-го бита
n ^ (1 << k)  — переключение k-го бита

Применение:
- Проверка чётности/нечётности
- Подсчёт единичных битов
- Обмен значений без временной переменной
- Нахождение одиночного элемента
- Подмножества (bitmask)
- Маски и флаги
"""


# ===== ОСНОВНЫЕ ОПЕРАЦИИ =====

def is_even(n):
    """
    Проверка чётности числа.
    
    n & 1 == 0 если n чётное.
    n & 1 == 1 если n нечётное.
    
    Сложность: O(1)
    """
    return (n & 1) == 0


def get_bit(n, k):
    """
    Получить значение k-го бита числа n.
    
    Аргументы:
        n: число
        k: позиция бита (0 = младший бит)
    
    Возвращает:
        int: 0 или 1
    
    Сложность: O(1)
    """
    return (n >> k) & 1


def set_bit(n, k):
    """
    Установить k-й бит в 1.
    
    Сложность: O(1)
    """
    return n | (1 << k)


def clear_bit(n, k):
    """
    Сбросить k-й бит в 0.
    
    Сложность: O(1)
    """
    return n & ~(1 << k)


def toggle_bit(n, k):
    """
    Переключить k-й бит.
    
    Сложность: O(1)
    """
    return n ^ (1 << k)


# ===== ПОДСЧЁТ БИТОВ =====

def count_bits_naive(n):
    """
    Подсчёт единичных битов (наивный способ).
    
    Проверяем каждый бит по очереди.
    
    Сложность: O(количество битов) = O(log n)
    """
    count = 0
    while n:
        count += n & 1
        n >>= 1
    return count


def count_bits_brian_kernighan(n):
    """
    Подсчёт единичных битов (алгоритм Брайана Кернигана).
    
    n & (n-1) сбрасывает младший единичный бит.
    Повторяем, пока n != 0.
    
    Сложность: O(количество единичных битов)
    
    Пример:
        n = 13 = 1101
        n & (n-1) = 1101 & 1100 = 1100 (сбросили бит 0)
        n & (n-1) = 1100 & 1011 = 1000 (сбросили бит 2)
        n & (n-1) = 1000 & 0111 = 0000 (сбросили бит 3)
        Итого: 3 единичных бита
    """
    count = 0
    while n:
        n &= n - 1
        count += 1
    return count


def count_bits_builtin(n):
    """
    Подсчёт единичных битов через встроенную функцию.
    
    bin(n).count('1') — простой способ.
    """
    return bin(n).count('1')


# ===== СТЕПЕНЬ ДВОЙКИ =====

def is_power_of_two(n):
    """
    Проверка, является ли число степенью двойки.
    
    Степень двойки имеет ровно один единичный бит.
    n & (n-1) == 0 для степеней двойки.
    
    Примеры:
        8 = 1000, 7 = 0111, 8 & 7 = 0 ✓
        6 = 0110, 5 = 0101, 6 & 5 = 0100 ≠ 0
    
    Сложность: O(1)
    """
    return n > 0 and (n & (n - 1)) == 0


def next_power_of_two(n):
    """
    Найти следующую степень двойки, большую или равную n.
    
    Сложность: O(log n)
    """
    if n <= 1:
        return 1
    
    n -= 1
    n |= n >> 1
    n |= n >> 2
    n |= n >> 4
    n |= n >> 8
    n |= n >> 16
    
    return n + 1


# ===== ОБМЕН ЗНАЧЕНИЙ =====

def swap_xor(a, b):
    """
    Обмен значений двух переменных без временной переменной.
    
    Использует свойство XOR:
    a ^ a = 0
    a ^ 0 = a
    
    Пример:
        a = 5 (101), b = 3 (011)
        a = a ^ b = 101 ^ 011 = 110 (6)
        b = a ^ b = 110 ^ 011 = 101 (5) — исходное a
        a = a ^ b = 110 ^ 101 = 011 (3) — исходное b
    
    Возвращает:
        tuple: (b, a)
    """
    a ^= b
    b ^= a
    a ^= b
    return a, b


# ===== ОДИНОЧНЫЙ ЭЛЕМЕНТ =====

def single_number(nums):
    """
    Найти элемент, встречающийся один раз.
    Все остальные встречаются дважды.
    
    Использует XOR:
    a ^ a = 0
    a ^ 0 = a
    XOR всех пар = 0, остаётся одиночный элемент.
    
    Аргументы:
        nums: список, где все элементы встречаются дважды, кроме одного
    
    Возвращает:
        int: одиночный элемент
    
    Сложность: O(n) по времени, O(1) по памяти
    
    Пример:
        >>> single_number([4, 1, 2, 1, 2])
        4
    """
    result = 0
    for num in nums:
        result ^= num
    return result


def single_number_iii(nums):
    """
    Найти два элемента, встречающихся по одному разу.
    Все остальные встречаются дважды.
    
    Аргументы:
        nums: список
    
    Возвращает:
        tuple: два одиночных элемента
    
    Сложность: O(n)
    """
    # XOR всех = a ^ b
    xor_all = 0
    for num in nums:
        xor_all ^= num
    
    # Находим любой различающийся бит
    diff_bit = xor_all & -xor_all  # Младший единичный бит
    
    # Разделяем числа на две группы
    a, b = 0, 0
    for num in nums:
        if num & diff_bit:
            a ^= num
        else:
            b ^= num
    
    return a, b


# ===== ОБРАТНЫЙ ПОРЯДОК БИТОВ =====

def reverse_bits(n, bits=32):
    """
    Обратить порядок битов числа.
    
    Аргументы:
        n: число
        bits: количество битов (по умолчанию 32)
    
    Возвращает:
        int: число с обращёнными битами
    
    Сложность: O(bits)
    """
    result = 0
    for _ in range(bits):
        result = (result << 1) | (n & 1)
        n >>= 1
    return result


# ===== ПОДМНОЖЕСТВА (BITMASK) =====

def generate_subsets(nums):
    """
    Сгенерировать все подмножества множества.
    
    Каждое подмножество кодируется битовой маской.
    i-й бит = 1 означает, что nums[i] включено в подмножество.
    
    Аргументы:
        nums: список элементов
    
    Возвращает:
        list: список всех подмножеств
    
    Сложность: O(2^n × n)
    
    Пример:
        >>> generate_subsets([1, 2, 3])
        [[], [1], [2], [1, 2], [3], [1, 3], [2, 3], [1, 2, 3]]
    """
    n = len(nums)
    subsets = []
    
    for mask in range(1 << n):
        subset = []
        for i in range(n):
            if mask & (1 << i):
                subset.append(nums[i])
        subsets.append(subset)
    
    return subsets


def subsets_with_bitmask(nums):
    """
    Подмножества с сохранением битовых масок.
    
    Возвращает:
        list: [(маска, подмножество), ...]
    """
    n = len(nums)
    result = []
    
    for mask in range(1 << n):
        subset = [nums[i] for i in range(n) if mask & (1 << i)]
        result.append((mask, subset))
    
    return result


# ===== ДИАПАЗОН ЧИСЕЛ =====

def missing_number(nums):
    """
    Найти пропущенное число в диапазоне [0, n].
    
    Дан массив длины n, содержащий числа от 0 до n без одного.
    
    Аргументы:
        nums: список чисел
    
    Возвращает:
        int: пропущенное число
    
    Сложность: O(n)
    
    Пример:
        >>> missing_number([3, 0, 1])
        2
    """
    n = len(nums)
    expected = n * (n + 1) // 2
    actual = sum(nums)
    return expected - actual


def missing_number_xor(nums):
    """
    Найти пропущенное число через XOR.
    
    Альтернативная реализация без переполнения.
    """
    n = len(nums)
    xor_all = 0
    
    for i in range(n + 1):
        xor_all ^= i
    
    for num in nums:
        xor_all ^= num
    
    return xor_all


# ===== ХАМИНГОВО РАССТОЯНИЕ =====

def hamming_distance(x, y):
    """
    Хэммингово расстояние между двумя числами.
    
    Количество битов, в которых числа различаются.
    
    Аргументы:
        x, y: числа
    
    Возвращает:
        int: расстояние Хэмминга
    
    Сложность: O(количество различающихся битов)
    
    Пример:
        >>> hamming_distance(1, 4)  # 001 vs 100
        2
    """
    return count_bits_brian_kernighan(x ^ y)


def total_hamming_distance(nums):
    """
    Сумма Хэмминговых расстояний между всеми парами.
    
    Аргументы:
        nums: список чисел
    
    Возвращает:
        int: сумма расстояний
    
    Сложность: O(32 × n) = O(n)
    """
    total = 0
    n = len(nums)
    
    for i in range(32):
        count_ones = sum((num >> i) & 1 for num in nums)
        count_zeros = n - count_ones
        total += count_ones * count_zeros
    
    return total


# ===== СУММА БЕЗ АРИФМЕТИКИ =====

def get_sum(a, b):
    """
    Сложение двух чисел без операторов + и -.
    
    Использует битовые операции:
    - XOR даёт сумму без учёта переносов
    - AND << 1 даёт переносы
    
    Повторяем, пока не станет переносов.
    
    Аргументы:
        a, b: числа
    
    Возвращает:
        int: a + b
    
    Сложность: O(количество битов)
    """
    mask = 0xFFFFFFFF  # Для работы с отрицательными числами
    
    while b != 0:
        carry = (a & b) << 1
        a = (a ^ b) & mask
        b = carry & mask
    
    # Обработка отрицательных чисел
    if a > 0x7FFFFFFF:
        a = ~(a ^ mask)
    
    return a


# ===== УМНОЖЕНИЕ =====

def multiply(a, b):
    """
    Умножение двух чисел через битовые операции.
    
    Русский крестьянский метод умножения.
    
    Сложность: O(log b)
    """
    result = 0
    while b > 0:
        if b & 1:
            result += a
        a <<= 1
        b >>= 1
    return result


