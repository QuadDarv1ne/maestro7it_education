'''
Задание 7: shalost_udalas.py
Автор: Дуплей Максим Игоревич / Дарья Руслановна Оборотова

Задача:
Даны два слова.
Нужно взять:
- из первого слова — минимальную букву (по лексикографическому порядку, то есть самую «раннюю» в алфавите),
- из второго слова — максимальную букву (самую «позднюю» в алфавите).

Если эти две буквы равны, или если мы сделаем наоборот (из первого — максимальную, из второго — минимальную) и они тоже равны — вывести "Удалась!".
В противном случае — вывести "В другой раз.".

Пример ввода:
негодник
хорошо

Вывод:
Удалась!

Пример ввода:
программирование
тестирование

Вывод:
В другой раз.
'''

word1 = input().strip()
word2 = input().strip()

# Берем минимальную букву из первого слова и максимальную из второго
min_first = min(word1)
max_second = max(word2)

# Берем максимальную букву из первого слова и минимальную из второго
max_first = max(word1)
min_second = min(word2)

# Проверяем условия
if min_first == max_second or max_first == min_second:
    print("Удалась!")
else:
    print("В другой раз.")