def solve():
    """
    Функция решает задачу A «Мейпл и Умножение» (Codeforces 2139).

    Условие:
    У Мейпл есть два положительных числа a и b.
    Разрешается любое количество раз (включая ноль) выбрать положительное число x
    и умножить либо a, либо b на x.
    Требуется определить минимальное количество операций, чтобы a стало равно b.

    Алгоритм:
    1. Если a == b, то операций не требуется → 0.
    2. Если одно число делится на другое (a % b == 0 или b % a == 0), 
       то достаточно одной операции.
    3. В остальных случаях всегда можно сделать их равными за две операции:
       умножить a на b и b на a → оба станут равны a*b.
    
    Ответ всегда равен 0, 1 или 2.

    Входные данные:
    - Первая строка: t — количество тестов (1 ≤ t ≤ 100).
    - Далее t строк: два целых положительных числа a, b (1 ≤ a,b ≤ 1000).

    Выходные данные:
    - Для каждого теста вывести минимальное количество операций.

    Пример:
    Ввод:
    3
    1 2
    10 3
    1000 1000
    Вывод:
    1
    2
    0
    """
    t = int(input())
    for _ in range(t):
        a, b = map(int, input().split())
        if a == b:
            print(0)
        elif a % b == 0 or b % a == 0:
            print(1)
        else:
            print(2)

