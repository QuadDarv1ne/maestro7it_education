def solve():
    """
    Решение задачи A: Разрежь массив
    
    Задача:
    Дан массив из n целых чисел. Необходимо разрезать его на три непустые части:
    префикс (с 1 по l элемент), средняя часть (с l+1 по r элемент) и суффикс (с r+1 по n).
    Требуется найти такие индексы l и r (1 ≤ l < r < n), чтобы остатки от деления сумм
    этих частей на 3 были либо все одинаковыми, либо все разными.
    
    Входные данные:
    - Первая строка: количество тестовых случаев t
    - Для каждого теста:
        * Первая строка: целое число n (длина массива)
        * Вторая строка: n целых чисел (сам массив)
    
    Выходные данные:
    - Для каждого теста выводит два числа l и r (в 1-индексации), удовлетворяющие условию,
      или "0 0", если подходящего разреза не существует.
    
    Пример:
    Ввод: 
        4
        6
        1 2 3 4 5 6
        4
        1 3 3 7
        3
        2 1 0
        5
        7 2 6 2 4
    
    Вывод:
        3 5
        0 0
        1 2
        2 4
    """
    n = int(input().strip())
    a = list(map(int, input().split()))
    
    # Ищем подходящие l и r (в 1-based индексации)
    for l in range(1, n-1):  # 1-based l от 1 до n-2
        for r in range(l+1, n):  # 1-based r от l+1 до n-1
            # Сумма префикса [0:l] в 0-based (1-based индексы 1..l)
            s1 = sum(a[:l]) % 3
            # Сумма средней части [l:r] в 0-based (1-based индексы l+1..r)
            s2 = sum(a[l:r]) % 3
            # Сумма суффикса [r:n] в 0-based (1-based индексы r+1..n)
            s3 = sum(a[r:]) % 3
            
            # Проверяем условия: все одинаковые или все разные
            if (s1 == s2 == s3) or (s1 != s2 and s2 != s3 and s1 != s3):
                print(l, r)
                return
    
    # Если не нашли подходящего разреза
    print("0 0")

t = int(input().strip())
for _ in range(t):
    solve()

