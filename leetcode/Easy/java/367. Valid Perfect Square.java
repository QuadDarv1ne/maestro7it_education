class Solution {
    /**
     * Проверяет, является ли число точным квадратом, с помощью бинарного поиска.
     * Используем long для mid, чтобы избежать переполнения при mid*mid.
     *
     * @param num положительное целое число
     * @return true, если num — точный квадрат
     */
    public boolean isPerfectSquare(int num) {
        long left = 1;
        long right = num;
        
        while (left <= right) {
            long mid = left + (right - left) / 2;
            long square = mid * mid;
            
            if (square == num) {
                return true;
            } else if (square < num) {
                left = mid + 1;
            } else {
                right = mid - 1;
            }
        }
        
        return false;
    }
}