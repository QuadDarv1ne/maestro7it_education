class Solution {
    /**
     * Генерирует массив строк FizzBuzz для чисел от 1 до n.
     *
     * @param n верхняя граница диапазона
     * @return массив строк согласно правилам FizzBuzz
     */
    public List<String> fizzBuzz(int n) {
        List<String> answer = new ArrayList<>();
        for (int i = 1; i <= n; i++) {
            if (i % 15 == 0) { // делится на 3 и 5
                answer.add("FizzBuzz");
            } else if (i % 3 == 0) {
                answer.add("Fizz");
            } else if (i % 5 == 0) {
                answer.add("Buzz");
            } else {
                answer.add(String.valueOf(i));
            }
        }
        return answer;
    }
}