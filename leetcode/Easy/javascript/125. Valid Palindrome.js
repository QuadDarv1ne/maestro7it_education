var isPalindrome = function(s) {
    let i = 0, j = s.length - 1;
    const isAlnum = ch => /[0-9a-zA-Z]/.test(ch);
    while (i < j) {
        while (i < j && !isAlnum(s[i])) i++;
        while (i < j && !isAlnum(s[j])) j--;
        if (i < j) {
            if (s[i].toLowerCase() !== s[j].toLowerCase()) return false;
            i++; j--;
        }
    }
    return true;
};

