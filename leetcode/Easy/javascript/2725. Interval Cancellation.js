/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    // 1. Выполняем функцию немедленно (time: 0)
    fn(...args);

    // 2. Запускаем интервал для последующих вызовов
    // setInterval автоматически передаст args в fn
    const intervalId = setInterval(fn, t, ...args);

    // 3. Возвращаем функцию отмены
    return () => clearInterval(intervalId);
};