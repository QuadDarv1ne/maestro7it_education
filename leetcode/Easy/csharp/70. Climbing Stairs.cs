public class Solution {
    /// <summary>
    /// –í—ã—á–∏—Å–ª—è–µ—Ç —á–∏—Å–ª–æ —Å–ø–æ—Å–æ–±–æ–≤ –ø–æ–¥–Ω—è—Ç—å—Å—è –Ω–∞ –≤–µ—Ä—à–∏–Ω—É –ª–µ—Å—Ç–Ω–∏—Ü—ã –∏–∑ n —Å—Ç—É–ø–µ–Ω–µ–π,
    /// –ø—Ä–∏ —É—Å–ª–æ–≤–∏–∏, —á—Ç–æ –º–æ–∂–Ω–æ –ø–æ–¥–Ω–∏–º–∞—Ç—å—Å—è –Ω–∞ 1 –∏–ª–∏ 2 —Å—Ç—É–ø–µ–Ω–∏ –∑–∞ —Ö–æ–¥.
    /// 
    /// –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –æ–ø—Ç–∏–º–∏–∑–∏—Ä–æ–≤–∞–Ω–Ω–æ–µ –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–æ–µ –ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ –ø–æ –§–∏–±–æ–Ω–∞—á—á–∏:
    /// ways[n] = ways[n-1] + ways[n-2], —Ö—Ä–∞–Ω–∏–º —Ç–æ–ª—å–∫–æ –¥–≤–∞ –ø—Ä–µ–¥—ã–¥—É—â–∏—Ö –∑–Ω–∞—á–µ–Ω–∏—è.
    /// </summary>
    /// <param name="n">–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—É–ø–µ–Ω–µ–π</param>
    /// <returns>–ß–∏—Å–ª–æ —Å–ø–æ—Å–æ–±–æ–≤ –ø–æ–¥–Ω—è—Ç—å—Å—è –Ω–∞ n —Å—Ç—É–ø–µ–Ω–µ–π</returns>
    public int ClimbStairs(int n) {
        if (n <= 1) return 1;
        int first = 1, second = 2;
        for (int i = 3; i <= n; i++) {
            int third = first + second;
            first = second;
            second = third;
        }
        return second;
    }
}

