class Solution:
    def mergeTwoLists(self, l1, l2):
        """
        –û–±—ä–µ–¥–∏–Ω—è–µ—Ç –¥–≤–∞ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö –æ–¥–Ω–æ—Å–≤—è–∑–Ω—ã—Ö —Å–ø–∏—Å–∫–∞ –≤ –æ–¥–∏–Ω –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π.

        Args:
            l1 (ListNode): –≥–æ–ª–æ–≤–∞ –ø–µ—Ä–≤–æ–≥–æ —Å–ø–∏—Å–∫–∞.
            l2 (ListNode): –≥–æ–ª–æ–≤–∞ –≤—Ç–æ—Ä–æ–≥–æ —Å–ø–∏—Å–∫–∞.

        Returns:
            ListNode: –≥–æ–ª–æ–≤–∞ –Ω–æ–≤–æ–≥–æ –æ–±—ä–µ–¥–∏–Ω—ë–Ω–Ω–æ–≥–æ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞.

        –ê–ª–≥–æ—Ä–∏—Ç–º:
        - –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Ñ–∏–∫—Ç–∏–≤–Ω–∞—è –≥–æ–ª–æ–≤–∞ dummy –∏ tail –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è –∫–æ–Ω—Ü–∞ —Å–ø–∏—Å–∫–∞.
        - –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –æ–±–æ–∏–º —Å–ø–∏—Å–∫–∞–º, —Å–æ–µ–¥–∏–Ω—è—è –º–µ–Ω—å—à–∏–π —ç–ª–µ–º–µ–Ω—Ç.
        - –ü–æ—Å–ª–µ –æ—Å–Ω–æ–≤–Ω–æ–≥–æ —Ü–∏–∫–ª–∞ –ø—Ä–∏—Å–æ–µ–¥–∏–Ω—è–µ–º –æ—Å—Ç–∞—Ç–æ–∫.

        –°–ª–æ–∂–Ω–æ—Å—Ç–∏:
        - –í—Ä–µ–º—è: O(m + n), –≥–¥–µ m, n ‚Äî –¥–ª–∏–Ω—ã —Å–ø–∏—Å–∫–æ–≤.
        - –ü–∞–º—è—Ç—å: O(1), –∏—Å–ø–æ–ª—å–∑—É—é—Ç—Å—è —Ç–æ–ª—å–∫–æ —É–∫–∞–∑–∞—Ç–µ–ª–∏.
        """
        dummy = tail = ListNode(0)
        while l1 and l2:
            if l1.val <= l2.val:
                tail.next = l1
                l1 = l1.next
            else:
                tail.next = l2
                l2 = l2.next
            tail = tail.next
        tail.next = l1 if l1 else l2
        return dummy.next

