class Solution:
    def hasAlternatingBits(self, n):
        """
        Определяет, чередуются ли биты в двоичном представлении числа.

        Алгоритм использует свойство: если у числа все соседние биты различны,
        то при выполнении операции XOR числа с самим собой, сдвинутым на 1 бит вправо,
        получится число, состоящее только из единиц (вида 2^k - 1).
        Затем проверяется, является ли это число + 1 степенью двойки.

        Args:
            n: Положительное целое число (1 <= n <= 2^31 - 1).

        Returns:
            True, если двоичное представление числа имеет чередующиеся биты,
            иначе False.

        Примеры:
            >>> Solution().hasAlternatingBits(5)  # 101
            True
            >>> Solution().hasAlternatingBits(7)  # 111
            False
            >>> Solution().hasAlternatingBits(11) # 1011
            False

        Сложность:
            Время: O(1) — константное число битовых операций.
            Память: O(1) — используются только несколько переменных.
        """
        # Вычисляем XOR числа с его копией, сдвинутой на один бит вправо.
        # Для числа с чередующимися битами (например, 101) эта операция даст число из всех единиц (111).
        xor_result = n ^ (n >> 1)

        # Для числа из всех единиц (вида 2^k - 1) верно, что (x & (x + 1)) == 0.
        # Например: 7 (111) & 8 (1000) = 0.
        # Это свойство проверяет, является ли xor_result + 1 степенью двойки,
        # что эквивалентно тому, что xor_result состоит из всех единиц.
        return (xor_result & (xor_result + 1)) == 0