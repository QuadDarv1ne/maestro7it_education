class Solution:
    def isPalindrome(self, x):
        """
        –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ x –ø–∞–ª–∏–Ω–¥—Ä–æ–º–æ–º –±–µ–∑ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è –≤ —Å—Ç—Ä–æ–∫—É.

        –î–µ–π—Å—Ç–≤–∏—è:
        - –û—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã–µ —á–∏—Å–ª–∞ –∏ —á–∏—Å–ª–∞, –æ–∫–∞–Ω—á–∏–≤–∞—é—â–∏–µ—Å—è –Ω–∞ 0 (–∫—Ä–æ–º–µ 0) ‚Üí False.
        - –ü–µ—Ä–µ–≤–æ—Ä–∞—á–∏–≤–∞–µ–º —Ü–∏—Ñ—Ä—ã —Å –∫–æ–Ω—Ü–∞ –ø–æ –æ–¥–Ω–æ–π, –ø–æ–∫–∞ –ø–µ—Ä–µ–≤—ë—Ä–Ω—É—Ç–∞—è —á–∞—Å—Ç—å –º–µ–Ω—å—à–µ –æ—Å—Ç–∞–ª—å–Ω–æ–π.
        - –í –∫–æ–Ω—Ü–µ —Å—Ä–∞–≤–Ω–∏–≤–∞–µ–º: x == rev (—á—ë—Ç–Ω–∞—è) –∏–ª–∏ x == rev // 10 (–Ω–µ—á—ë—Ç–Ω–∞—è).

        –í—Ä–µ–º—è: O(log‚ÇÅ‚ÇÄ(x)), –ø–∞–º—è—Ç—å: O(1).
        """
        if x < 0 or (x % 10 == 0 and x != 0):
            return False
        rev = 0
        while x > rev:
            rev = rev * 10 + x % 10
            x //= 10
        return x == rev or x == rev // 10

