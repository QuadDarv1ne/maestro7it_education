class Solution:
    def evalRPN(self, tokens):
        """
        Вычисляет выражение в обратной польской записи (Reverse Polish Notation).
        
        Обратная польская запись — это математическая нотация, в которой операторы 
        следуют за своими операндами. Например:
        - ["2","1","+","3","*"] → (2 + 1) * 3 = 9
        - ["4","13","5","/","+"] → 4 + (13 / 5) = 6
        
        Параметры:
        ----------
        tokens : List[str]
            Список строк, представляющих токены выражения.
            Каждый токен может быть:
            - Целым числом (в виде строки)
            - Оператором: "+" (сложение), "-" (вычитание), "*" (умножение), "/" (деление)
        
        Возвращает:
        ----------
        int
            Результат вычисления выражения.
            
        Алгоритм:
        ---------
        1. Использует стек для хранения операндов.
        2. Проходит по всем токенам:
           - Если токен - число, помещает его в стек.
           - Если токен - оператор:
             * Извлекает два последних числа из стека.
             * Применяет оператор (учитывая порядок операндов для вычитания и деления).
             * Помещает результат обратно в стек.
        3. В конце в стеке остается один элемент - результат вычисления.
        
        Особенности:
        ------------
        - Деление округляется к нулю (целочисленное деление).
        - Поддерживаются только операторы +, -, *, /.
        
        Примеры:
        --------
        >>> evalRPN(["2","1","+","3","*"])
        9
        >>> evalRPN(["4","13","5","/","+"])
        6
        >>> evalRPN(["10","6","9","3","+","-11","*","/","*","17","+","5","+"])
        22
        """
        stack = []
        
        for token in tokens:
            if token not in {"+", "-", "*", "/"}:
                stack.append(int(token))
            else:
                # Извлекаем операнды: первый - правый, второй - левый
                b = stack.pop()
                a = stack.pop()
                
                if token == "+":
                    stack.append(a + b)
                elif token == "-":
                    stack.append(a - b)
                elif token == "*":
                    stack.append(a * b)
                else:  # token == "/"
                    # Целочисленное деление с округлением к нулю
                    # int(a / b) работает правильно, в отличие от a // b
                    stack.append(int(a / b))
        
        return stack.pop()