var lengthOfLIS = function(nums) {
    const tails = [];

    for (let num of nums) {
        // –î–≤–æ–∏—á–Ω—ã–π –ø–æ–∏—Å–∫ –ø–æ–∑–∏—Ü–∏–∏ –¥–ª—è –≤—Å—Ç–∞–≤–∫–∏ num –≤ –º–∞—Å—Å–∏–≤ tails
        let left = 0, right = tails.length;
        while (left < right) {
            const mid = Math.floor((left + right) / 2);
            if (tails[mid] < num) {
                left = mid + 1;
            } else {
                right = mid;
            }
        }

        if (left === tails.length) {
            tails.push(num);
        } else {
            tails[left] = num;
        }
    }

    return tails.length;
};

