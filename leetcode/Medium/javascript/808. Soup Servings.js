/**
 * –í—ã—á–∏—Å–ª—è–µ—Ç –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ, —á—Ç–æ —Å—É–ø A –∑–∞–∫–æ–Ω—á–∏—Ç—Å—è —Ä–∞–Ω—å—à–µ —Å—É–ø–∞ B,
 * –ø–ª—é—Å –ø–æ–ª–æ–≤–∏–Ω–∞ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏, —á—Ç–æ –æ–Ω–∏ –∑–∞–∫–æ–Ω—á–∞—Ç—Å—è –æ–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ.
 *
 * –ò–¥–µ—è:
 * - –ú–∞—Å—à—Ç–∞–±–∏—Ä—É–µ–º –æ–±—ä—ë–º –∫—Ä–∞—Ç–Ω–æ 25 –º–ª: m = ceil(n / 25).
 * - –†–µ–∫—É—Ä—Å–∏–≤–Ω—ã–π DFS —Å –º–µ–º–æ–∏–∑–∞—Ü–∏–µ–π –ø–æ —Å–æ—Å—Ç–æ—è–Ω–∏—è–º (a, b) ‚Äî —Å–∫–æ–ª—å–∫–æ ¬´—á–µ—Ç–≤–µ—Ä—Ç—É—à–µ–∫¬ª –æ—Å—Ç–∞–ª–æ—Å—å.
 * - –ë–∞–∑—ã:
 *     a <= 0 –∏ b <= 0 ‚Üí 0.5
 *     a <= 0 ‚Üí 1.0
 *     b <= 0 ‚Üí 0.0
 * - –ü–µ—Ä–µ—Ö–æ–¥:
 *     dfs(a,b) = 0.25 * [ dfs(a-4,b) + dfs(a-3,b-1) + dfs(a-2,b-2) + dfs(a-1,b-3) ]
 * - –î–ª—è –±–æ–ª—å—à–∏—Ö n (‚âà > 4800) –æ—Ç–≤–µ—Ç —Å—Ç—Ä–µ–º–∏—Ç—Å—è –∫ 1.0 ‚Üí –≤–æ–∑–≤—Ä–∞—â–∞–µ–º 1.0.
 *
 * –°–ª–æ–∂–Ω–æ—Å—Ç—å:
 * - –ü–æ —á–∏—Å–ª—É —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö —Å–æ—Å—Ç–æ—è–Ω–∏–π O(m^2), –≥–¥–µ m = ceil(n/25).
 * - –ü–∞–º—è—Ç—å O(m^2) –Ω–∞ –∫—ç—à.
 *
 * @param {number} n - –º–∏–ª–ª–∏–ª–∏—Ç—Ä—ã –∫–∞–∂–¥–æ–≥–æ –≤–∏–¥–∞ —Å—É–ø–∞ (0 ‚â§ n ‚â§ 1e9)
 * @return {number} –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å (–ø–æ–≥—Ä–µ—à–Ω–æ—Å—Ç—å ‚â§ 1e-5)
 */
var soupServings = function(n) {
  if (n > 4800) return 1.0;

  const m = Math.ceil(n / 25);
  const memo = new Map();

  function key(a, b) { return a + ',' + b; }

  function dfs(a, b) {
    if (a <= 0 && b <= 0) return 0.5;
    if (a <= 0) return 1.0;
    if (b <= 0) return 0.0;

    const k = key(a, b);
    if (memo.has(k)) return memo.get(k);

    const res = 0.25 * (
      dfs(a - 4, b) +
      dfs(a - 3, b - 1) +
      dfs(a - 2, b - 2) +
      dfs(a - 1, b - 3)
    );
    memo.set(k, res);
    return res;
  }

  return dfs(m, m);
};

