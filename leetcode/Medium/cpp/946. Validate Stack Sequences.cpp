#include <vector>
#include <stack>
using namespace std;

class Solution {
public:
    /**
     * –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç–∏ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ –æ–ø–µ—Ä–∞—Ü–∏–π —Å–æ —Å—Ç–µ–∫–æ–º.
     * 
     * @param pushed - –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å —á–∏—Å–µ–ª, –∫–æ—Ç–æ—Ä—ã–µ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ –ø–æ–º–µ—â–∞—é—Ç—Å—è –≤ —Å—Ç–µ–∫
     * @param popped - –ø—Ä–µ–¥–ø–æ–ª–∞–≥–∞–µ–º–∞—è –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –∏–∑–≤–ª–µ—á–µ–Ω–∏—è –∏–∑ —Å—Ç–µ–∫–∞
     * @return true, –µ—Å–ª–∏ popped –º–æ–∂–Ω–æ –ø–æ–ª—É—á–∏—Ç—å –∏–∑ pushed —Å –ø–æ–º–æ—â—å—é –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã—Ö –æ–ø–µ—Ä–∞—Ü–∏–π —Å—Ç–µ–∫–∞ (LIFO)
     * 
     * –ê–ª–≥–æ—Ä–∏—Ç–º:
     * 1. –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –º–∞—Å—Å–∏–≤—É pushed –∏ –∫–ª–∞–¥—ë–º —ç–ª–µ–º–µ–Ω—Ç—ã –≤ —Å—Ç–µ–∫.
     * 2. –ö–∞–∂–¥—ã–π —Ä–∞–∑, –∫–æ–≥–¥–∞ –≤–µ—Ä—Ö—É—à–∫–∞ —Å—Ç–µ–∫–∞ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å —Ç–µ–∫—É—â–∏–º —ç–ª–µ–º–µ–Ω—Ç–æ–º popped,
     *    –≤—ã—Ç–∞–ª–∫–∏–≤–∞–µ–º –µ—ë –∏ –¥–≤–∏–≥–∞–µ–º —É–∫–∞–∑–∞—Ç–µ–ª—å –ø–æ popped.
     * 3. –ï—Å–ª–∏ –≤ –∫–æ–Ω—Ü–µ —É–¥–∞–ª–æ—Å—å –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã popped ‚Äî –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –∫–æ—Ä—Ä–µ–∫—Ç–Ω–∞.
     * 
     * –í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: O(n), –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç –ø–æ–º–µ—â–∞–µ—Ç—Å—è –∏ –∏–∑–≤–ª–µ–∫–∞–µ—Ç—Å—è –º–∞–∫—Å–∏–º—É–º 1 —Ä–∞–∑.
     * –ü–∞–º—è—Ç—å: O(n) –¥–ª—è –≤—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω–æ–≥–æ —Å—Ç–µ–∫–∞.
     */
    bool validateStackSequences(vector<int>& pushed, vector<int>& popped) {
        stack<int> st;
        int j = 0;
        for (int x : pushed) {
            st.push(x);
            while (!st.empty() && st.top() == popped[j]) {
                st.pop();
                j++;
            }
        }
        return j == popped.size();
    }
};

