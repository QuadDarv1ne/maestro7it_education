/**
 * Возвращает k-ю счастливую строку длины n в лексикографическом порядке.
 *
 * Счастливая строка содержит только символы 'a', 'b', 'c' и не имеет двух одинаковых
 * соседних символов. Если общее количество таких строк (3 * 2^(n-1)) меньше k,
 * возвращается пустая строка.
 *
 * Реализация: итеративное построение результата. На каждом шаге для текущей позиции
 * перебираются допустимые символы (не равные предыдущему) в порядке 'a', 'b', 'c'.
 * Если количество строк, начинающихся с выбранного префикса (2^(n - i - 1)),
 * меньше k, то этот префикс пропускается (k уменьшается). Иначе символ фиксируется.
 *
 * @param n длина строки (1 ≤ n ≤ 10)
 * @param k порядковый номер строки (1-индексация, 1 ≤ k ≤ 100)
 * @return k-я счастливая строка или пустая строка
 */
class Solution {
public:
    string getHappyString(int n, int k) {
        // Общее количество счастливых строк = 3 * 2^(n-1)
        int total = 3 * (1 << (n - 1)); // 2^(n-1) через битовый сдвиг
        if (k > total) return "";

        string result;
        char prev = 0; // предыдущий символ (0 означает, что его еще нет)

        for (int i = 0; i < n; i++) {
            // Перебираем символы в лексикографическом порядке
            for (char c : {'a', 'b', 'c'}) {
                if (c == prev) continue; // пропускаем повторяющиеся

                // Количество строк с текущим префиксом
                // Для оставшихся позиций: каждая может быть любым из двух (кроме предыдущего)
                int count = 1 << (n - i - 1); // 2^(n - i - 1)

                if (k > count) {
                    // Искомая строка не в этом блоке
                    k -= count;
                } else {
                    // Нашли нужный символ
                    result.push_back(c);
                    prev = c;
                    break;
                }
            }
        }
        return result;
    }
};