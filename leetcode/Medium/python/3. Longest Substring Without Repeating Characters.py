class Solution:
    def lengthOfLongestSubstring(self, s):
        """
        –ù–∞—Ö–æ–¥–∏—Ç –¥–ª–∏–Ω—É —Å–∞–º–æ–π –¥–ª–∏–Ω–Ω–æ–π –ø–æ–¥—Å—Ç—Ä–æ–∫–∏ –±–µ–∑ –ø–æ–≤—Ç–æ—Ä—è—é—â–∏—Ö—Å—è —Å–∏–º–≤–æ–ª–æ–≤.

        –ê–ª–≥–æ—Ä–∏—Ç–º:
        - –ú–µ—Ç–æ–¥ —Å–∫–æ–ª—å–∑—è—â–µ–≥–æ –æ–∫–Ω–∞ (sliding window).
        - last_seen —Ö—Ä–∞–Ω–∏—Ç –ø–æ—Å–ª–µ–¥–Ω–∏–π –∏–Ω–¥–µ–∫—Å –ø–æ—è–≤–ª–µ–Ω–∏—è —Å–∏–º–≤–æ–ª–∞ (–∏–ª–∏ -1, –µ—Å–ª–∏ –Ω–µ –≤—Å—Ç—Ä–µ—á–∞–ª—Å—è).
        - j ‚Äî –∏–Ω–¥–µ–∫—Å –ø–µ—Ä–µ–¥ –Ω–∞—á–∞–ª–æ–º —Ç–µ–∫—É—â–µ–≥–æ –æ–∫–Ω–∞.
        - –î–ª—è –∫–∞–∂–¥–æ–≥–æ —Å–∏–º–≤–æ–ª–∞ s[i]:
            * –ï—Å–ª–∏ —Å–∏–º–≤–æ–ª –≤—Å—Ç—Ä–µ—á–∞–ª—Å—è, —Å–¥–≤–∏–≥–∞–µ–º j –≤–ø—Ä–∞–≤–æ: j = max(j, last_seen[—Å–∏–º–≤–æ–ª]).
            * –í—ã—á–∏—Å–ª—è–µ–º –¥–ª–∏–Ω—É –æ–∫–Ω–∞: i - j.
            * –û–±–Ω–æ–≤–ª—è–µ–º –º–∞–∫—Å–∏–º—É–º.
            * –ó–∞–ø–æ–º–∏–Ω–∞–µ–º –∏–Ω–¥–µ–∫—Å —Ç–µ–∫—É—â–µ–≥–æ —Å–∏–º–≤–æ–ª–∞.
        –°–ª–æ–∂–Ω–æ—Å—Ç—å:
        - –í—Ä–µ–º—è: O(n)
        - –ü–∞–º—è—Ç—å: O(1) –¥–ª—è —Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞ ASCII.
        
        :param s: –≤—Ö–æ–¥–Ω–∞—è —Å—Ç—Ä–æ–∫–∞
        :return: –¥–ª–∏–Ω–∞ —Å–∞–º–æ–π –¥–ª–∏–Ω–Ω–æ–π –ø–æ–¥—Å—Ç—Ä–æ–∫–∏ –±–µ–∑ –ø–æ–≤—Ç–æ—Ä–æ–≤
        """
        last_seen = {}
        ans = 0
        j = -1
        for i, c in enumerate(s):
            if c in last_seen:
                j = max(j, last_seen[c])
            ans = max(ans, i - j)
            last_seen[c] = i
        return ans

