class Solution:
    def letterCombinations(self, digits):
        """
        –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –≤—Å–µ –≤–æ–∑–º–æ–∂–Ω—ã–µ –∫–æ–º–±–∏–Ω–∞—Ü–∏–∏ –±—É–∫–≤ –¥–ª—è –∑–∞–¥–∞–Ω–Ω–æ–π —Å—Ç—Ä–æ–∫–∏ digits.
        """
        if not digits:
            return []

        digit_map = {
            '2': 'abc', '3': 'def', '4': 'ghi',
            '5': 'jkl', '6': 'mno', '7': 'pqrs',
            '8': 'tuv', '9': 'wxyz'
        }

        def backtrack(index, current):
            if index == len(digits):
                combinations.append(current)
                return
            for letter in digit_map[digits[index]]:
                backtrack(index + 1, current + letter)

        combinations = []
        backtrack(0, "")
        return combinations

