class Solution:
    def removeNthFromEnd(self, head, n):
        """
        –£–¥–∞–ª—è–µ—Ç n-–π —É–∑–µ–ª —Å –∫–æ–Ω—Ü–∞ –æ–¥–Ω–æ—Å–≤—è–∑–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞.

        –ê–ª–≥–æ—Ä–∏—Ç–º:
        - –ò—Å–ø–æ–ª—å–∑—É–µ–º —Ñ–∏–∫—Ç–∏–≤–Ω—É—é –≥–æ–ª–æ–≤—É dummy –¥–ª—è —É–ø—Ä–æ—â–µ–Ω–∏—è —É–¥–∞–ª–µ–Ω–∏—è
        - –ò—Å–ø–æ–ª—å–∑—É–µ–º –¥–≤–∞ —É–∫–∞–∑–∞—Ç–µ–ª—è: fast –∏ slow
        - –ü–µ—Ä–µ–¥–≤–∏–≥–∞–µ–º fast –Ω–∞ n+1 —à–∞–≥ –≤–ø–µ—Ä—ë–¥
        - –î–≤–∏–≥–∞–µ–º –æ–±–∞ —É–∫–∞–∑–∞—Ç–µ–ª—è –Ω–∞ –æ–¥–∏–Ω —à–∞–≥, –ø–æ–∫–∞ fast –Ω–µ –¥–æ—Å—Ç–∏–≥–Ω–µ—Ç –∫–æ–Ω—Ü–∞
        - slow.next ‚Äî —É–∑–µ–ª, –∫–æ—Ç–æ—Ä—ã–π –Ω—É–∂–Ω–æ —É–¥–∞–ª–∏—Ç—å
        - –ü—Ä–∏—Å–≤–∞–∏–≤–∞–µ–º slow.next = slow.next.next

        –í—Ä–µ–º—è: O(L), L ‚Äî –¥–ª–∏–Ω–∞ —Å–ø–∏—Å–∫–∞
        –ü–∞–º—è—Ç—å: O(1)

        :param head: ListNode ‚Äî –≥–æ–ª–æ–≤–∞ —Å–ø–∏—Å–∫–∞
        :param n: int ‚Äî –Ω–æ–º–µ—Ä —É–∑–ª–∞ —Å –∫–æ–Ω—Ü–∞, –∫–æ—Ç–æ—Ä—ã–π –Ω—É–∂–Ω–æ —É–¥–∞–ª–∏—Ç—å
        :return: ListNode ‚Äî –≥–æ–ª–æ–≤–∞ –æ–±–Ω–æ–≤–ª—ë–Ω–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞
        """
        dummy = ListNode(0)
        dummy.next = head
        fast = slow = dummy

        for _ in range(n + 1):
            fast = fast.next

        while fast:
            fast = fast.next
            slow = slow.next

        slow.next = slow.next.next
        return dummy.next

