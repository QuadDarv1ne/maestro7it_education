"""
Задача: Минимальное количество секунд для уменьшения высоты горы до нуля

Условие:
Вам дана целочисленная переменная mountainHeight, обозначающая высоту горы.
Также дан целочисленный массив workerTimes, где workerTimes[i] — время работы i-го рабочего в секундах.

Рабочие работают одновременно, чтобы уменьшить высоту горы. Для рабочего i:
- Чтобы уменьшить высоту горы на x, требуется время:
  workerTimes[i] + workerTimes[i] * 2 + ... + workerTimes[i] * x секунд.
  Например:
  - Для уменьшения высоты на 1 требуется workerTimes[i] секунд.
  - Для уменьшения высоты на 2 требуется workerTimes[i] + workerTimes[i] * 2 секунд.
  - И так далее.

Необходимо вернуть целое число — минимальное количество секунд, за которое все рабочие,
работая одновременно, смогут уменьшить высоту горы до нуля.

Функция:
minNumberOfSeconds(mountainHeight: int, workerTimes: List[int]) -> int

Входные параметры:
    mountainHeight (int): Начальная высота горы. (1 <= mountainHeight <= 10^5)
    workerTimes (List[int]): Массив времени работников. (1 <= len(workerTimes) <= 10^4, 1 <= workerTimes[i] <= 10^6)

Выходные данные:
    int: Минимальное время в секундах.

Примеры:
1.  Вход: mountainHeight = 4, workerTimes = [2,1,1]
    Выход: 3
    Пояснение: Один из способов уменьшить высоту до 0:
        - Рабочий 0 уменьшает высоту на 1, затрачивая 2 сек.
        - Рабочий 1 уменьшает высоту на 2, затрачивая 1 + 1*2 = 3 сек.
        - Рабочий 2 уменьшает высоту на 1, затрачивая 1 сек.
        Так как они работают одновременно, общее время равно max(2, 3, 1) = 3 сек.

2.  Вход: mountainHeight = 10, workerTimes = [3,2,2,4]
    Выход: 12
    Пояснение:
        - Рабочий 0 уменьшает высоту на 2, затрачивая 3 + 3*2 = 9 сек.
        - Рабочий 1 уменьшает высоту на 3, затрачивая 2 + 2*2 + 2*3 = 12 сек.
        - Рабочий 2 уменьшает высоту на 3, затрачивая 12 сек.
        - Рабочий 3 уменьшает высоту на 2, затрачивая 4 + 4*2 = 12 сек.
        Общее время: max(9, 12, 12, 12) = 12 сек.

3.  Вход: mountainHeight = 5, workerTimes = [1]
    Выход: 15
    Пояснение: Единственный рабочий, поэтому ответ — сумма: 1 + 1*2 + 1*3 + 1*4 + 1*5 = 15 сек.

Подсказка по решению:
Задача эффективно решается с помощью бинарного поиска по количеству секунд.
Для фиксированного времени T можно определить, какую максимальную суммарную высоту
смогут снять все рабочие. Для каждого рабочего i максимальная высота x_i,
которую он успевает снять за время T, удовлетворяет неравенству:
workerTimes[i] * (1 + 2 + ... + x_i) <= T.
Так как 1+2+...+x = x*(x+1)/2, условие принимает вид:
workerTimes[i] * x*(x+1)/2 <= T.
Зная T, нужно проверить, что сумма всех x_i >= mountainHeight.
Бинарный поиск ищет минимальное T, при котором условие выполняется.
"""

class Solution:
    def minNumberOfSeconds(self, mountainHeight, workerTimes):
        # Собственная реализация целочисленного квадратного корня (floor(sqrt(n)))
        def int_sqrt(n):
            if n < 2:
                return n
            x = n // 2
            while True:
                y = (x + n // x) // 2
                if y >= x:
                    return x
                x = y

        def can_reduce(T):
            total = 0
            for w in workerTimes:
                # Максимальная высота, которую снимет рабочий за время T
                # Решаем неравенство: w * x * (x+1) // 2 <= T
                # Дискриминант: 1 + 8 * (T // w)
                d = 1 + 8 * (T // w)
                sqrt_d = int_sqrt(d)
                x = (sqrt_d - 1) // 2
                total += x
                if total >= mountainHeight:
                    return True
            return total >= mountainHeight

        left, right = 0, 10**18  # достаточно большая верхняя граница
        while left < right:
            mid = (left + right) // 2
            if can_reduce(mid):
                right = mid
            else:
                left = mid + 1
        return left