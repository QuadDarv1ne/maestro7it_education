# from typing import List

class Solution:
    def findDuplicate(self, nums):
        # Фаза 1: обнаружение цикла
        slow = nums[0]
        fast = nums[0]
        while True:
            slow = nums[slow]
            fast = nums[nums[fast]]
            if slow == fast:
                break

        # Фаза 2: поиск входа в цикл (дубликата)
        slow = nums[0]
        while slow != fast:
            slow = nums[slow]
            fast = nums[fast]

        return slow