"""
Задача: Подсчёт подматриц с равной частотой X и Y, содержащих верхний левый элемент
(LeetCode 3212. Count Submatrices With Equal Frequency of X and Y)

Условие:
    Дана символьная матрица grid размером m x n, содержащая символы 'X', 'Y', '.'.
    Необходимо найти количество подматриц, которые:
    1. Содержат элемент grid[0][0] (т.е. имеют левый верхний угол в (0,0)).
    2. Имеют равное количество символов 'X' и 'Y'.
    3. Содержат хотя бы один символ 'X'.

Подход:
    Все подматрицы однозначно определяются своим правым нижним углом (i, j).
    Для каждой такой подматрицы нужно быстро вычислить количество X и Y.
    Используется метод двумерных префиксных сумм (2D prefix sum) для обоих символов.

Алгоритм:
    1. Инициализируем две матрицы prefixX и prefixY того же размера, заполненные 0.
    2. Для каждой ячейки (i, j) от 0 до m-1, от 0 до n-1:
       a. Базовое значение переносится из ячеек сверху (i-1, j) и слева (i, j-1).
       b. Вычитается значение из диагональной ячейки (i-1, j-1) для устранения двойного счёта.
       c. Если текущий символ в grid[i][j] равен 'X' или 'Y', соответствующая префиксная сумма увеличивается на 1.
       d. После вычисления prefixX[i][j] и prefixY[i][j] проверяем:
          - prefixX[i][j] == prefixY[i][j] (равное количество X и Y)
          - prefixX[i][j] > 0 (есть хотя бы один X)
          Если условия выполнены, увеличиваем счётчик на 1.
    3. Возвращаем счётчик.

Сложность:
    Время: O(m * n) — один проход по всем элементам матрицы.
    Память: O(m * n) — для хранения двух матриц префиксных сумм.
    (Возможна оптимизация до O(n) с использованием скользящей строки, но для ясности реализован базовый вариант.)

Возвращаемое значение:
    int — количество подматриц, удовлетворяющих условиям.

Пример 1:
    grid = [["X","Y","."],["Y",".","."]]
    Префиксные суммы для X и Y:
    (0,0): X=1,Y=0 -> нет
    (0,1): X=1,Y=1 -> да (X>0)
    (0,2): X=1,Y=1 -> да
    (1,0): X=1,Y=1 -> да
    (1,1): X=1,Y=2 -> нет
    (1,2): X=1,Y=2 -> нет
    Итого: 3

Пример 2:
    grid = [["X","X"],["X","Y"]]
    Ни одна подматрица не имеет равного количества X и Y -> результат 0
"""

class Solution:
    def numberOfSubmatrices(self, grid):
        m, n = len(grid), len(grid[0])
        # Массивы для хранения количества X и Y в префиксе
        prefix_x = [[0] * n for _ in range(m)]
        prefix_y = [[0] * n for _ in range(m)]

        count = 0
        for i in range(m):
            for j in range(n):
                # Копируем значения из предыдущих ячеек
                if i > 0:
                    prefix_x[i][j] += prefix_x[i-1][j]
                    prefix_y[i][j] += prefix_y[i-1][j]
                if j > 0:
                    prefix_x[i][j] += prefix_x[i][j-1]
                    prefix_y[i][j] += prefix_y[i][j-1]
                if i > 0 and j > 0:
                    # Вычитаем дважды учтённую область
                    prefix_x[i][j] -= prefix_x[i-1][j-1]
                    prefix_y[i][j] -= prefix_y[i-1][j-1]

                # Добавляем текущий символ
                if grid[i][j] == 'X':
                    prefix_x[i][j] += 1
                elif grid[i][j] == 'Y':
                    prefix_y[i][j] += 1

                # Проверяем условия
                if prefix_x[i][j] == prefix_y[i][j] and prefix_x[i][j] > 0:
                    count += 1
        return count