    def jump(self, nums):
        """
        –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä—ã–∂–∫–æ–≤, –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã—Ö –¥–ª—è –¥–æ—Å—Ç–∏–∂–µ–Ω–∏—è
        –∫–æ–Ω—Ü–∞ –º–∞—Å—Å–∏–≤–∞, –≥–¥–µ –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç –º–∞—Å—Å–∏–≤–∞ nums[i] ‚Äî –º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –¥–ª–∏–Ω–∞
        –ø—Ä—ã–∂–∫–∞ —Å –ø–æ–∑–∏—Ü–∏–∏ i.

        –ü–∞—Ä–∞–º–µ—Ç—Ä—ã:
        -----------
        nums : List[int]
            –°–ø–∏—Å–æ–∫ —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª, –≥–¥–µ nums[i] ‚Äî –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ –≤–æ–∑–º–æ–∂–Ω—ã–π —à–∞–≥ –∏–∑ –ø–æ–∑–∏—Ü–∏–∏ i.

        –í–æ–∑–≤—Ä–∞—â–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ:
        ----------------------
        int
            –ú–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –ø—Ä—ã–∂–∫–æ–≤, —á—Ç–æ–±—ã –¥–æ–±—Ä–∞—Ç—å—Å—è –¥–æ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –∏–Ω–¥–µ–∫—Å–∞ –º–∞—Å—Å–∏–≤–∞.

        –û–ø–∏—Å–∞–Ω–∏–µ —Ä–µ—à–µ–Ω–∏—è:
        -----------------
        –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –∂–∞–¥–Ω—ã–π –∞–ª–≥–æ—Ä–∏—Ç–º:
        - –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –º–∞—Å—Å–∏–≤—É, –æ—Ç—Å–ª–µ–∂–∏–≤–∞—è –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é –¥–∞–ª—å–Ω–æ—Å—Ç—å –ø—Ä—ã–∂–∫–∞ (farthest),
          –∫–æ—Ç–æ—Ä—É—é –º–æ–∂–Ω–æ –¥–æ—Å—Ç–∏–≥–Ω—É—Ç—å —Å —Ç–µ–∫—É—â–µ–≥–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –ø—Ä—ã–∂–∫–æ–≤.
        - current_end ‚Äî –≥—Ä–∞–Ω–∏—Ü–∞ —Ç–µ–∫—É—â–µ–≥–æ –¥–∏–∞–ø–∞–∑–æ–Ω–∞ –ø—Ä—ã–∂–∫–æ–≤; –∫–æ–≥–¥–∞ –¥–æ—Å—Ç–∏–≥–∞–µ–º –µ—ë,
          —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫ –ø—Ä—ã–∂–∫–æ–≤ –∏ –æ–±–Ω–æ–≤–ª—è–µ–º –≥—Ä–∞–Ω–∏—Ü—É –Ω–∞ farthest.
        - –¢–∞–∫–∏–º –æ–±—Ä–∞–∑–æ–º –º–∏–Ω–∏–º–∏–∑–∏—Ä—É–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä—ã–∂–∫–æ–≤.

        –°–ª–æ–∂–Ω–æ—Å—Ç—å:
        ----------
        –í—Ä–µ–º–µ–Ω–Ω–∞—è: O(n), –≥–¥–µ n ‚Äî –¥–ª–∏–Ω–∞ –º–∞—Å—Å–∏–≤–∞ nums.
        –ü–æ –ø–∞–º—è—Ç–∏: O(1).

        –ü—Ä–∏–º–µ—Ä:
        --------
        nums = [2, 3, 1, 1, 4]
        –†–µ–∑—É–ª—å—Ç–∞—Ç: 2
        –ü–æ—è—Å–Ω–µ–Ω–∏–µ: –ø—Ä—ã–∂–æ–∫ —Å –ø–æ–∑–∏—Ü–∏–∏ 0 –Ω–∞ 1, –∑–∞—Ç–µ–º —Å –ø–æ–∑–∏—Ü–∏–∏ 1 –Ω–∞ 4 (–∫–æ–Ω–µ—Ü –º–∞—Å—Å–∏–≤–∞).
        """
        jumps = 0
        current_end = 0
        farthest = 0
        for i in range(len(nums) - 1):
            farthest = max(farthest, i + nums[i])
            if i == current_end:
                jumps += 1
                current_end = farthest
        return jumps

