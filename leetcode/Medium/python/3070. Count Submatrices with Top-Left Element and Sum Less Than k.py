"""
Задача: Подсчет подматриц с верхним левым элементом и суммой ≤ k
(LeetCode 3070. Count Submatrices with Top-Left Element and Sum Less Than k)

Условие:
    Дана целочисленная матрица grid размером m x n и целое число k.
    Необходимо найти количество подматриц, которые:
    1. Содержат верхний левый элемент grid[0][0] (то есть начинаются в ячейке (0,0)).
    2. Сумма всех элементов подматрицы ≤ k.

Подход:
    Все подматрицы, удовлетворяющие условию, являются прямоугольниками,
    простирающимися от (0,0) до некоторой ячейки (i, j).
    Задача сводится к подсчету количества ячеек (i, j), для которых
    сумма элементов в прямоугольнике (0,0) -> (i,j) не превышает k.

    Для эффективного вычисления сумм используется метод двумерных префиксных сумм
    (2D prefix sum), примененный непосредственно к исходной матрице для экономии памяти.

Алгоритм:
    1. Проходим по всем ячейкам матрицы (i от 0 до m-1, j от 0 до n-1).
    2. Для каждой ячейки вычисляем префиксную сумму:
       - Если i == 0 и j == 0: оставляем значение без изменений.
       - Если i == 0 (первая строка): grid[i][j] = grid[i][j] + grid[i][j-1]
       - Если j == 0 (первый столбец): grid[i][j] = grid[i][j] + grid[i-1][j]
       - Иначе: grid[i][j] = grid[i][j] + grid[i-1][j] + grid[i][j-1] - grid[i-1][j-1]
    3. После вычисления префиксной суммы для текущей ячейки,
       проверяем условие grid[i][j] ≤ k.
    4. Если условие выполняется, увеличиваем счетчик.

Сложность:
    Время: O(m * n) - один проход по матрице.
    Память: O(1) - используется исходная матрица (модифицируется на месте).

Возвращаемое значение:
    int - количество подходящих подматриц.

Пример 1:
    grid = [[7,6,3],[6,6,1]], k = 18
    Префиксные суммы:
    [7, 13, 16]
    [13, 25, 29]
    Условию ≤18 удовлетворяют: 7, 13, 16, 13 → результат 4.

Пример 2:
    grid = [[7,2,9],[1,5,0],[2,6,6]], k = 20
    Результат: 6
"""

class Solution:
    def countSubmatrices(self, grid, k):
        m, n = len(grid), len(grid[0])
        count = 0
        # Модифицируем grid на месте, чтобы хранить префиксные суммы
        for i in range(m):
            for j in range(n):
                # Сумма для верхней строки
                if i == 0 and j == 0:
                    current_sum = grid[i][j]
                # Сумма для первого столбца
                elif i == 0:
                    current_sum = grid[i][j] + grid[i][j-1]
                    grid[i][j] = current_sum
                # Сумма для первой строки
                elif j == 0:
                    current_sum = grid[i][j] + grid[i-1][j]
                    grid[i][j] = current_sum
                # Сумма для всех остальных ячеек
                else:
                    current_sum = grid[i][j] + grid[i-1][j] + grid[i][j-1] - grid[i-1][j-1]
                    grid[i][j] = current_sum

                if current_sum <= k:
                    count += 1
        return count