class Solution:
    def maxArea(self, height):
        """
        –ù–∞—Ö–æ–¥–∏—Ç –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é –ø–ª–æ—â–∞–¥—å –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞ –¥–ª—è –≤–æ–¥—ã.

        –î–∞–Ω—ã n –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω—ã—Ö –ª–∏–Ω–∏–π —Å –≤—ã—Å–æ—Ç–∞–º–∏ height[i]. 
        –ù—É–∂–Ω–æ –≤—ã–±—Ä–∞—Ç—å –¥–≤–µ –ª–∏–Ω–∏–∏ —Ç–∞–∫, —á—Ç–æ–±—ã –æ–Ω–∏ –≤–º–µ—Å—Ç–µ —Å –æ—Å—å—é X –æ–±—Ä–∞–∑–æ–≤–∞–ª–∏ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä, 
        –∫–æ—Ç–æ—Ä—ã–π –≤–º–µ—â–∞–µ—Ç –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–æ–¥—ã.

        –ê–ª–≥–æ—Ä–∏—Ç–º:
        - –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–≤–∞ —É–∫–∞–∑–∞—Ç–µ–ª—è: left –≤ –Ω–∞—á–∞–ª–µ, right –≤ –∫–æ–Ω—Ü–µ –º–∞—Å—Å–∏–≤–∞.
        - –ù–∞ –∫–∞–∂–¥–æ–º —à–∞–≥–µ –≤—ã—á–∏—Å–ª—è–µ—Ç—Å—è –ø–ª–æ—â–∞–¥—å: 
            area = (right - left) * min(height[left], height[right]).
        - –£–∫–∞–∑–∞—Ç–µ–ª—å —Å –º–µ–Ω—å—à–µ–π –≤—ã—Å–æ—Ç–æ–π –¥–≤–∏–≥–∞–µ—Ç—Å—è –≤–Ω—É—Ç—Ä—å, 
          —Ç–∞–∫ –∫–∞–∫ –∏–º–µ–Ω–Ω–æ –æ–Ω –æ–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ—Ç –≤–æ–∑–º–æ–∂–Ω—É—é –ø–ª–æ—â–∞–¥—å.
        - –†–∞–±–æ—Ç–∞–µ—Ç –∑–∞ O(n), —Ç—Ä–µ–±—É–µ—Ç O(1) –ø–∞–º—è—Ç–∏.

        :param height: —Å–ø–∏—Å–æ–∫ –≤—ã—Å–æ—Ç –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω—ã—Ö –ª–∏–Ω–∏–π
        :return: –º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –ø–ª–æ—â–∞–¥—å –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞
        """
        left, right = 0, len(height) - 1
        max_area = 0
        while left < right:
            area = (right - left) * min(height[left], height[right])
            max_area = max(max_area, area)
            if height[left] < height[right]:
                left += 1
            else:
                right -= 1
        return max_area

