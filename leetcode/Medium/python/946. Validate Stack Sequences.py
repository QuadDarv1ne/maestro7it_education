class Solution:
    def validateStackSequences(self, pushed, popped):
        """
        –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –º–æ–∂–Ω–æ –ª–∏ –∏–∑ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ pushed
        –ø–æ–ª—É—á–∏—Ç—å –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å popped —Å –ø–æ–º–æ—â—å—é –æ–ø–µ—Ä–∞—Ü–∏–π —Å—Ç–µ–∫–∞.
        
        :param pushed: —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª, –∫–æ—Ç–æ—Ä—ã–µ –∫–ª–∞–¥—É—Ç—Å—è –≤ —Å—Ç–µ–∫
        :param popped: —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª, –∫–æ—Ç–æ—Ä—ã–µ –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å –∏–∑–≤–ª–µ—á–µ–Ω—ã
        :return: True, –µ—Å–ª–∏ popped –¥–æ—Å—Ç–∏–∂–∏–º, –∏–Ω–∞—á–µ False

        –ê–ª–≥–æ—Ä–∏—Ç–º:
        1. –î–æ–±–∞–≤–ª—è–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –∏–∑ pushed –≤ —Å—Ç–µ–∫.
        2. –ï—Å–ª–∏ –≤–µ—Ä—Ö—É—à–∫–∞ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å —Ç–µ–∫—É—â–∏–º —ç–ª–µ–º–µ–Ω—Ç–æ–º popped,
           –∏–∑–≤–ª–µ–∫–∞–µ–º –∏ –¥–≤–∏–≥–∞–µ–º —É–∫–∞–∑–∞—Ç–µ–ª—å –ø–æ popped.
        3. –í –∫–æ–Ω—Ü–µ –ø—Ä–æ–≤–µ—Ä—è–µ–º, –≤—Å–µ –ª–∏ —ç–ª–µ–º–µ–Ω—Ç—ã popped –±—ã–ª–∏ –∏–∑–≤–ª–µ—á–µ–Ω—ã.

        –í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: O(n) ‚Äî –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç –∫–ª–∞–¥—ë—Ç—Å—è –∏ –∏–∑–≤–ª–µ–∫–∞–µ—Ç—Å—è –º–∞–∫—Å–∏–º—É–º 1 —Ä–∞–∑.
        –ü–∞–º—è—Ç—å: O(n) –¥–ª—è —Å—Ç–µ–∫–∞.
        """
        stack, j = [], 0
        for x in pushed:
            stack.append(x)
            while stack and stack[-1] == popped[j]:
                stack.pop()
                j += 1
        return j == len(popped)

