/**
 * –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —É–∑–ª–∞ —Å–≤—è–∑–∞–Ω–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞ (—É–∂–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–æ –≤ LeetCode, –Ω–µ –Ω—É–∂–Ω–æ –ø–µ—Ä–µ–æ–ø—Ä–µ–¥–µ–ª—è—Ç—å).
 * public class ListNode {
 *     public int val;
 *     public ListNode next;
 *     public ListNode(int val=0, ListNode next=null) {
 *         this.val = val;
 *         this.next = next;
 *     }
 * }
 */

public class Solution {
    /**
     * –°–∫–ª–∞–¥—ã–≤–∞–µ—Ç –¥–≤–∞ —á–∏—Å–ª–∞, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–Ω—ã—Ö —Å–≤—è–∑–∞–Ω–Ω—ã–º–∏ —Å–ø–∏—Å–∫–∞–º–∏, –≥–¥–µ —Ü–∏—Ñ—Ä—ã
     * —Ö—Ä–∞–Ω—è—Ç—Å—è –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ.
     * –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—É–º–º—É –≤ –≤–∏–¥–µ —Å–≤—è–∑–∞–Ω–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞.
     * 
     * @param l1 –ü–µ—Ä–≤—ã–π —Å–≤—è–∑–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è—é—â–∏–π –ø–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ.
     * @param l2 –í—Ç–æ—Ä–æ–π —Å–≤—è–∑–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è—é—â–∏–π –≤—Ç–æ—Ä–æ–µ —á–∏—Å–ª–æ.
     * @return –°–≤—è–∑–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è—é—â–∏–π —Å—É–º–º—É –¥–≤—É—Ö —á–∏—Å–µ–ª.
     */
    public ListNode AddTwoNumbers(ListNode l1, ListNode l2) {
        ListNode dummy = new ListNode(0); // –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–π —É–∑–µ–ª –¥–ª—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
        ListNode current = dummy;
        int carry = 0; // –ü–µ—Ä–µ–Ω–æ—Å –∏–∑ —Å—Ç–∞—Ä—à–µ–≥–æ —Ä–∞–∑—Ä—è–¥–∞

        while (l1 != null || l2 != null || carry != 0) {
            int val1 = (l1 != null) ? l1.val : 0; // –ó–Ω–∞—á–µ–Ω–∏–µ –∏–∑ –ø–µ—Ä–≤–æ–≥–æ —Å–ø–∏—Å–∫–∞ –∏–ª–∏ 0
            int val2 = (l2 != null) ? l2.val : 0; // –ó–Ω–∞—á–µ–Ω–∏–µ –∏–∑ –≤—Ç–æ—Ä–æ–≥–æ —Å–ø–∏—Å–∫–∞ –∏–ª–∏ 0

            int sum = val1 + val2 + carry; // –°—É–º–º–∞ —Ç–µ–∫—É—â–∏—Ö —Ü–∏—Ñ—Ä –∏ –ø–µ—Ä–µ–Ω–æ—Å–∞
            carry = sum / 10; // –û–±–Ω–æ–≤–ª—è–µ–º –ø–µ—Ä–µ–Ω–æ—Å
            current.next = new ListNode(sum % 10); // –°–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —É–∑–µ–ª —Å —Ç–µ–∫—É—â–µ–π —Ü–∏—Ñ—Ä–æ–π
            current = current.next; // –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —É–∑–ª—É

            if (l1 != null) l1 = l1.next; // –ü–µ—Ä–µ—Ö–æ–¥ –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —ç–ª–µ–º–µ–Ω—Ç—É –ø–µ—Ä–≤–æ–≥–æ —Å–ø–∏—Å–∫–∞
            if (l2 != null) l2 = l2.next; // –ü–µ—Ä–µ—Ö–æ–¥ –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —ç–ª–µ–º–µ–Ω—Ç—É –≤—Ç–æ—Ä–æ–≥–æ —Å–ø–∏—Å–∫–∞
        }

        return dummy.next; // –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç, –ø—Ä–æ–ø—É—Å–∫–∞—è –≤—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–π —É–∑–µ–ª
    }
}

