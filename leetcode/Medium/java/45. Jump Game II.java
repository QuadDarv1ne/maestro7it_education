public class Solution {
    /**
     * –ù–∞—Ö–æ–¥–∏—Ç –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä—ã–∂–∫–æ–≤ –¥–ª—è –¥–æ—Å—Ç–∏–∂–µ–Ω–∏—è –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –∏–Ω–¥–µ–∫—Å–∞.
     *
     * @param nums –º–∞—Å—Å–∏–≤, –≥–¥–µ nums[i] ‚Äî –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π –ø—Ä—ã–∂–æ–∫ —Å –ø–æ–∑–∏—Ü–∏–∏ i.
     * @return –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –ø—Ä—ã–∂–∫–æ–≤ –¥–æ –∫–æ–Ω—Ü–∞ –º–∞—Å—Å–∏–≤–∞.
     *
     * –ê–ª–≥–æ—Ä–∏—Ç–º:
     * –ñ–∞–¥–Ω—ã–π –ø–æ–¥—Ö–æ–¥:
     * - farthest ‚Äî –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π –∏–Ω–¥–µ–∫—Å, –¥–æ—Å—Ç–∏–∂–∏–º—ã–π —Ç–µ–∫—É—â–∏–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º –ø—Ä—ã–∂–∫–æ–≤.
     * - current_end ‚Äî –≥—Ä–∞–Ω–∏—Ü–∞ —Ç–µ–∫—É—â–µ–π –æ–±–ª–∞—Å—Ç–∏ –ø—Ä—ã–∂–∫–æ–≤.
     * - –ö–æ–≥–¥–∞ –∏–Ω–¥–µ–∫—Å –¥–æ—Å—Ç–∏–≥–∞–µ—Ç current_end, —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º jumps –∏ –æ–±–Ω–æ–≤–ª—è–µ–º current_end.
     *
     * –°–ª–æ–∂–Ω–æ—Å—Ç—å:
     * - –í—Ä–µ–º—è: O(n), –≥–¥–µ n ‚Äî –¥–ª–∏–Ω–∞ –º–∞—Å—Å–∏–≤–∞ nums.
     * - –ü–∞–º—è—Ç—å: O(1).
     *
     * –ü—Ä–∏–º–µ—Ä:
     * nums = [2, 3, 1, 1, 4]
     * –û—Ç–≤–µ—Ç: 2 (–ø—Ä—ã–∂–∫–∏ —Å 0 –Ω–∞ 1 –∏ —Å 1 –Ω–∞ 4).
     */
    public int jump(int[] nums) {
        int jumps = 0;
        int current_end = 0;
        int farthest = 0;

        for (int i = 0; i < nums.length - 1; i++) {
            farthest = Math.max(farthest, i + nums[i]);
            if (i == current_end) {
                jumps++;
                current_end = farthest;
            }
        }
        return jumps;
    }
}

