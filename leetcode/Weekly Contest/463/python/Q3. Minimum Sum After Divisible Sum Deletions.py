class Solution:
    def minArraySum(self, a, k):
        t=0
        for x in a: t+=x
        b=[-inf]*k
        b[0]=0
        d=0
        p=0
        for x in a:
            p+=x
            r=p%k
            v=b[r]+p
            if d<v: d=v
            w=d-p
            if b[r]<w: b[r]=w
        return t-d

