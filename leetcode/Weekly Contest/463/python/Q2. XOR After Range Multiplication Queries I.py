class Solution:
    def xorAfterQueries(self, a, q):
        M=1000000007
        for l,r,k,v in q:
            i=l
            while i<=r:
                a[i]=(a[i]*v)%M
                i+=k
        x=0
        for y in a:
            x^=y
        return x

