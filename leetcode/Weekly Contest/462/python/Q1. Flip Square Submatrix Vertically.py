class Solution:
    def reverseSubmatrix(self, grid, x, y, k):
        """
        –ü–µ—Ä–µ–≤–æ—Ä–∞—á–∏–≤–∞–µ—Ç (–ø–æ –≤–µ—Ä—Ç–∏–∫–∞–ª–∏) –∫–≤–∞–¥—Ä–∞—Ç–Ω—É—é –ø–æ–¥–º–∞—Ç—Ä–∏—Ü—É k√ók –≤ –º–∞—Ç—Ä–∏—Ü–µ grid.

        :param grid: –ú–∞—Ç—Ä–∏—Ü–∞ (m√ón) —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª.
        :param x: –ò–Ω–¥–µ–∫—Å —Å—Ç—Ä–æ–∫–∏ –≤–µ—Ä—Ö–Ω–µ–≥–æ –ª–µ–≤–æ–≥–æ —É–≥–ª–∞ –ø–æ–¥–º–∞—Ç—Ä–∏—Ü—ã.
        :param y: –ò–Ω–¥–µ–∫—Å —Å—Ç–æ–ª–±—Ü–∞ –≤–µ—Ä—Ö–Ω–µ–≥–æ –ª–µ–≤–æ–≥–æ —É–≥–ª–∞ –ø–æ–¥–º–∞—Ç—Ä–∏—Ü—ã.
        :param k: –†–∞–∑–º–µ—Ä —Å—Ç–æ—Ä–æ–Ω—ã –ø–æ–¥–º–∞—Ç—Ä–∏—Ü—ã.
        :return: –ú–∞—Ç—Ä–∏—Ü–∞ –ø–æ—Å–ª–µ –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω–æ–≥–æ –ø–µ—Ä–µ–≤–æ—Ä–æ—Ç–∞ –ø–æ–¥–º–∞—Ç—Ä–∏—Ü—ã.

        –ê–ª–≥–æ—Ä–∏—Ç–º:
        1. –ò–¥—ë–º —Å–≤–µ—Ä—Ö—É –≤–Ω–∏–∑ –¥–æ —Å–µ—Ä–µ–¥–∏–Ω—ã –ø–æ–¥–º–∞—Ç—Ä–∏—Ü—ã.
        2. –î–ª—è –∫–∞–∂–¥–æ–π –ø–∞—Ä—ã —Å—Ç—Ä–æ–∫ –≤–Ω—É—Ç—Ä–∏ –ø–æ–¥–º–∞—Ç—Ä–∏—Ü—ã –º–µ–Ω—è–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –º–µ—Å—Ç–∞–º–∏.
        3. –û—Å—Ç–∞–ª—å–Ω–∞—è –º–∞—Ç—Ä–∏—Ü–∞ –æ—Å—Ç–∞—ë—Ç—Å—è –Ω–µ–∏–∑–º–µ–Ω–Ω–æ–π.

        –°–ª–æ–∂–Ω–æ—Å—Ç—å:
        - –í—Ä–µ–º—è: O(k¬≤)
        - –ü–∞–º—è—Ç—å: O(1)
        """
        for i in range(k // 2):
            top_row = x + i
            bottom_row = x + k - 1 - i
            for j in range(k):
                col = y + j
                grid[top_row][col], grid[bottom_row][col] = grid[bottom_row][col], grid[top_row][col]
        return grid

