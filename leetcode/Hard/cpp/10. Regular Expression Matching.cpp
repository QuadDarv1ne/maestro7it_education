#include <string>
#include <vector>
using namespace std;

/**
 * @brief –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –ª–∏ —Å—Ç—Ä–æ–∫–∞ s —à–∞–±–ª–æ–Ω—É p,
 * –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞—é—â–µ–º—É '.' –∏ '*'.
 */
class Solution {
public:
    bool isMatch(string s, string p) {
        int m = s.size(), n = p.size();
        vector<vector<bool>> dp(m + 1, vector<bool>(n + 1, false));
        dp[0][0] = true;

        // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –¥–ª—è —à–∞–±–ª–æ–Ω–æ–≤ –≤–∏–¥–∞ a*, a*b*, –∏ —Ç.–¥.
        for (int j = 1; j <= n; ++j) {
            if (p[j - 1] == '*') dp[0][j] = dp[0][j - 2];
        }

        for (int i = 1; i <= m; ++i) {
            for (int j = 1; j <= n; ++j) {
                if (p[j - 1] == s[i - 1] || p[j - 1] == '.') {
                    dp[i][j] = dp[i - 1][j - 1];
                } else if (p[j - 1] == '*') {
                    dp[i][j] = dp[i][j - 2] || 
                               ((p[j - 2] == s[i - 1] || p[j - 2] == '.') && dp[i - 1][j]);
                }
            }
        }
        return dp[m][n];
    }
};

